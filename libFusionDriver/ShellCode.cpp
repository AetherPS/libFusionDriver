	int StartPThread(int processId, uint64_t entryPoint)
	{
		auto shellCodeHeader = (ThreadShellCodeHeader*)_binary_resources_ThreadShellCode_bin_start;
		shellCodeHeader->ShellCodeComplete = 0;
		shellCodeHeader->ThreadEntry = entryPoint; // Set the thread entry point.
		shellCodeHeader->scePthreadCreate = Resolve(processId, "libkernel", "scePthreadCreate");
		shellCodeHeader->scePthreadJoin = Resolve(processId, "libkernel", "scePthreadJoin");

		// Find the thread initial.
		shellCodeHeader->thr_initial = GetRemoteAddress(processId, "libkernel.sprx", 0x0008E430);

		if (shellCodeHeader->thr_initial == 0)
			shellCodeHeader->thr_initial = GetRemoteAddress(processId, "libkernel_sys.sprx", 0x0008E830);

		if (shellCodeHeader->thr_initial == 0)
			shellCodeHeader->thr_initial = GetRemoteAddress(processId, "libkernel_web.sprx", 0x0008E430);

		// Make sure that we actually found scePthreadCreate.
		if (shellCodeHeader->scePthreadCreate <= 0)
		{
			klog("StartPThread(): Failed to resolve scePthreadCreate.\n");
			return -1;
		}

		// Make sure that we actually found scePthreadJoin.
		if (shellCodeHeader->scePthreadJoin <= 0)
		{
			klog("StartPThread(): Failed to resolve scePthreadJoin.\n");
			return -1;
		}

		// Make sure that we actually found thr_initial.
		if (shellCodeHeader->thr_initial <= 0)
		{
			klog("StartPThread(): Failed to resolve thr_initial.\n");
			return -1;
		}

		auto totalAllocatedSize = THREAD_SIZE + STACK_SIZE;
		uint64_t shellCodeMemory;
		if (AllocateMemory(processId, &shellCodeMemory, totalAllocatedSize, PROT_READ | PROT_WRITE | PROT_EXEC, MAP_ANON | MAP_PREFAULT_READ) != 0 || (void*)shellCodeMemory == nullptr || (void*)shellCodeMemory == MAP_FAILED || shellCodeMemory < 0)
		{
			klog("StartPThread(): Failed to allocate memory on the process. %llX\n", shellCodeMemory);
			return -1;
		}

		if (ReadWriteMemory(processId, shellCodeMemory, shellCodeHeader, THREAD_SIZE, true) != 0)
		{
			klog("StartPThread(): Write shellcode failed.\n");
			FreeMemory(processId, shellCodeMemory, totalAllocatedSize);
			return false;
		}

		// Create a thread to run our shellcode.
		StartThread(processId, shellCodeMemory + shellCodeHeader->entry, shellCodeMemory + THREAD_SIZE, STACK_SIZE);

		// Wait for the shellcode to complete by reading the byte that will be set to 1 on completion.
		bool shellCodeComplete = false;
		while (!shellCodeComplete)
		{
			sceKernelUsleep(1000 * 500);

			if (ReadWriteMemory(processId, shellCodeMemory + offsetof(ThreadShellCodeHeader, ShellCodeComplete), (void*)&shellCodeComplete, sizeof(shellCodeComplete), false) != 0)
			{
				klog("StartPThread(): Failed to read shellCodeComplete.\n");
				FreeMemory(processId, shellCodeMemory, totalAllocatedSize);
				return -1;
			}
		}

		FreeMemory(processId, shellCodeMemory, totalAllocatedSize);

		return 0;
	}

	int LoadSprx(int processId, const char* path)
	{
		auto shellCodeHeader = (SprxLoaderHeader*)_binary_resources_LoaderShellCode_bin_start;
		shellCodeHeader->ModuleHandle = -1;
		strcpy(shellCodeHeader->Path, (char*)path);
		shellCodeHeader->sceKernelLoadStartModule = Resolve(processId, "libkernel", "sceKernelLoadStartModule");
		shellCodeHeader->scePthreadExit = Resolve(processId, "libkernel", "scePthreadExit");

		// Make sure that we actually found sceKernelLoadStartModule.
		if (shellCodeHeader->sceKernelLoadStartModule < 0)
		{
			klog("LoadSprx(): Failed to resolve sceKernelLoadStartModule.\n");
			return -1;
		}

		// Make sure that we actually found scePthreadExit.
		if (shellCodeHeader->scePthreadExit < 0)
		{
			klog("LoadSprx(): Failed to resolve scePthreadExit.\n");
			return -1;
		}

		uint64_t shellCodeMemory;
		if (AllocateMemory(processId, &shellCodeMemory, PRX_LOAD_SIZE, PROT_READ | PROT_WRITE | PROT_EXEC, MAP_ANON | MAP_PREFAULT_READ) != 0 || (void*)shellCodeMemory == nullptr || (void*)shellCodeMemory == MAP_FAILED || shellCodeMemory < 0)
		{
			klog("LoadSprx(): Failed to allocate memory on the process. %llX\n", shellCodeMemory);
			return -1;
		}

		if (ReadWriteMemory(processId, shellCodeMemory, shellCodeHeader, PRX_LOAD_SIZE, true) != 0)
		{
			klog("LoadSprx(): Write shellcode failed.\n");
			FreeMemory(processId, shellCodeMemory, PRX_LOAD_SIZE);
			return false;
		}

		if (StartPThread(processId, (uint64_t)(shellCodeMemory + shellCodeHeader->entry)) != 0)
		{
			klog("LoadSprx(): shellcode thread failed.\n");
			FreeMemory(processId, shellCodeMemory, PRX_LOAD_SIZE);
			return false;
		}

		// Read out the module handle to see if the loading was a success or not.
		int32_t moduleHandle = -1;
		if (ReadWriteMemory(processId, shellCodeMemory + offsetof(SprxLoaderHeader, ModuleHandle), (void*)&moduleHandle, sizeof(moduleHandle), false) != 0)
		{
			klog("LoadSprx(): Failed to read ModuleHandle.\n");
			FreeMemory(processId, shellCodeMemory, PRX_LOAD_SIZE);
			return -1;
		}

		// Free up the memory we dont need it anymore.
		FreeMemory(processId, shellCodeMemory, PRX_LOAD_SIZE);

		return moduleHandle;
	}

	int UnloadSprx(int processId, int handle)
	{
		auto shellCodeHeader = (SprxUnLoaderHeader*)_binary_resources_UnLoaderShellCode_bin_start;
		shellCodeHeader->ModuleHandle = handle;
		shellCodeHeader->Result = 0;
		shellCodeHeader->sceKernelStopUnloadModule = Resolve(processId, "libkernel", "sceKernelStopUnloadModule");
		shellCodeHeader->scePthreadExit = Resolve(processId, "libkernel", "scePthreadExit");

		// Make sure that we actually found sceKernelStopUnloadModule.
		if (shellCodeHeader->sceKernelStopUnloadModule == 0)
		{
			klog("UnloadSprx(): Failed to resolve sceKernelStopUnloadModule.");
			return -1;
		}

		// Make sure that we actually found scePthreadExit.
		if (shellCodeHeader->scePthreadExit == 0)
		{
			klog("UnloadSprx(): Failed to resolve scePthreadExit.");
			return -1;
		}

		uint64_t shellCodeMemory;
		if (AllocateMemory(processId, &shellCodeMemory, PRX_UNLOAD_SIZE, PROT_READ | PROT_WRITE | PROT_EXEC, MAP_ANON | MAP_PREFAULT_READ) != 0 || (void*)shellCodeMemory == nullptr || (void*)shellCodeMemory == MAP_FAILED || shellCodeMemory < 0)
		{
			klog("UnloadSprx(): Failed to allocate memory on the process. %llX\n", shellCodeMemory);
			return -1;
		}

		if (ReadWriteMemory(processId, shellCodeMemory, shellCodeHeader, PRX_UNLOAD_SIZE, true) != 0)
		{
			klog("UnloadSprx(): Write shellcode failed.\n");
			FreeMemory(processId, shellCodeMemory, PRX_UNLOAD_SIZE);
			return -1;
		}

		if (StartPThread(processId, (uint64_t)(shellCodeMemory + shellCodeHeader->entry)) != 0)
		{
			klog("UnloadSprx(): shellcode thread failed.\n");
			FreeMemory(processId, shellCodeMemory, PRX_UNLOAD_SIZE);
			return -1;
		}

		// Read the result from the shellcode to see if we succeeded.
		int32_t result = -1;
		if (ReadWriteMemory(processId, shellCodeMemory + offsetof(SprxUnLoaderHeader, Result), (void*)&result, sizeof(result), false) != 0)
		{
			klog("UnloadSprx(): Failed to read Result.\n");
			FreeMemory(processId, shellCodeMemory, PRX_UNLOAD_SIZE);
			return -1;
		}

		// Free up the memory we dont need it anymore.
		FreeMemory(processId, shellCodeMemory, PRX_UNLOAD_SIZE);

		return result;
	}
